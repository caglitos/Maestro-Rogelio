<?php
// Ejemplo de un ciclo for en PHP
echo "Ejemplo de un ciclo for en PHP\n<br />";
for ($i = 0; $i < 10; $i++) {
    echo "$i\n<br />";
}

echo "Ejemplo de While\n<br />";
$i = 0;
while ($i < 10) {
    echo "$i\n<br />";
    $i++;
}

echo "Ejemplo de Do While\n<br />";
$i = 0;
do {
    echo "$i\n<br />";
    $i++;
} while ($i < 10);
?>
